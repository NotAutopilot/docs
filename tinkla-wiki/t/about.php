<h3 class="title">Loving a good Challenge</h3>
</div>
<p>NOTHING can motivate me more than beeing told "this can't be done". While we were doing the early integration into the instrument cluster, one amazingly bright and well known person in the Tesla hacking community told me that emulating the DAS so that the car thinks it is there can not be done... Oh my did that make me super motivated and few weeks later I had it going. Same thing with the radar and emulating all the messages so it can be retrofitted to preAP cars... Another challenge was when Tesla started to lock down the MCU1 and we could not run our original softPanda software on the CID. That's when I created the <a class="thm-color-two" href="t/tinkla_buddy.html">Tinkla Buddy</a>. The more I got challenged, the more I learned about the complex interconnections between the Tesla ECUs, their messages and how the safety system works to ensure that the message are correct. With that knowledge I was able to create the <a class="thm-color-two" href="tinkla_relay">Tinkla Relay</a>. Then I retrofitted the whole AP1 system to my car and learned few more tricks in the process. All those items have helped improve the Tesla Unity branch of the OpenPilot. 
                </p></div>
