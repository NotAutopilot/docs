<h3 class="title">Passion Driven</h3>
</div>
<p>I started coding a long time ago... very long time ago... think Cobol and punched cards... That's a LONG time ago :)... I have used pretty much any programming language out there... I have designed and built any kind of software you can imagine: from simple stand-alone apps (desktop or iOS) to large complex distributed system. I have built software for large computers (even one that was run on a Cray) all the way to embeded system for life supporting medical devices. And pretty much anything in between. So you can easily say coding is one of my passions. The other one is cars. Fast cars especially. I love racing them. But when I started a 50-mile commute (each direction) a few years back on a heavily congested Interstate in Pennsylvania, I sarted looking at options to automate my drive. That is when I ran into the OpenPilot project (back then at version 0.4.2) and immediately got hooked into the community. Making OpenPilot work on preAP Tesla Model S, adding new capabilities, pushing the bounderies of the system... It was love at first sight and the rest is pretty much history.</p>
</div>
