<h2 class="post_title">Available March 2024</h2>
<div class="blog_meta">
<p>The complex (3 CAN intercepts) harness for interfacing with a Model 3/ Model Y is now in manufacturing and should be available in March 2024. The hardware prototypes have been tested and are ready for mass production. Now all it is left is to borrow a M3/MY and  bring the code live to Tesla Unity. The code will only run on a Comma 3 and will require the use of this complex harness which I estimate will cost somewere in the $700-$800 USD. The alternative will be for you to get two Black Pandas (2 x $299) and thee relay boxes (3 x $200) plus a bunch of connectors and plugs to make the harness work. My harness will be plug and play.</p>
<p>Why would you want OpenPilot on a M3/MY? In case you did not spend the 15k on FSD but you want to have more than just the basic lane keep. Because the future that OpenPilot can offer (faster than the much promised FSD), while is not as complete as dropping your car anywhere in the world and driving itself back to you, is more realistic and will happen a lot faster. </p>
<p>Features like lane keep assist, automatic lane changes, full stop-n-go, stopping and stop lights and stop signs are already available in the Tesla Unity branch. Navigate on maps will (as implemented in the main branch of Comma's OpenPilot) is also available.</p>
