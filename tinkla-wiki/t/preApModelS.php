<h2 class="post_title">Items Needed</h2>
<div class="blog_meta">
<h5 class="thm-color-one">Required Items</h5>
<ul>
<li>Pre-AutoPilot Tesla Model S (2012-2014)</li>
<li><a class="thm-color-two" href="https://comma.ai/shop/products/three" target="_new">$1499 USD Comma three devkit</a> or a used Comma two devkit, (if you can find one) either of them without harness. <b class="thm-color-one">Note:</b> Support for Comma two/EON Gold ended April 2022. Newer features on the branch (like stopping at stop signs and lights, navigate on maps in the future) are not be compatible with these devices. I will maintain a version of the software for C2 devices but they can not get the new features.</li>
<li><a class="thm-color-two" href="t/tinkla_obdc.html">Tinkla OBD-C Adapter</a></li>
<li><a class="thm-color-two" href="https://shop.tinkla.us/USB4-2m-Extension-Cable-For-Comma-Two-Three-p467071951" target="_new">2m USB4/Thunderbolt3</a> extension cable. Not all USB-C cables are the same.</li>
<li><a class="thm-color-two" href="">Tesla Unity</a> branch of the OpenPilot software.</li>
</ul>
<hr/>
<h5 class="thm-color-one">Optional Items</h5>
<ul>
<li><a class="thm-color-two" href="chassis_can_retrofit_harness">Tinkla Chassis CAN Retrofit Harness</a> if your OBD2 port does not have the Chassis CAN pins. Check our <a class="thm-color-two" href="index_php/Tesla_Model_S_preAP_OBD2_port.html">wiki</a> to see what you have to look for in order to check.</li>
<li><a class="thm-color-two" href="t/tinkla_buddy.html">Tinkla Buddy</a> for IC integration (only works with MCU1, no solution available for MCU2). If you have MCU2, you can still use OpenPilot without any issues, it will just not display the information on the Instrument Cluster, just on the Comma device screen.</li>
<li><a class="thm-color-two" href="tinkla_pedal">Tinkla Pedal Interceptor</a> for longitudinal control down to 5 MPH</li>
<li><a class="thm-color-two" href="tinkla_radar">Tinkla Radar Kit</a> for better detection of other vehicles</li>
<li><a class="thm-color-two" href="https://sghinnovations.com/?product=tesla-pre-ap-openpilot-ibooster-retrofit-kit" target="_new">SGH Innovations' iBooster with ECU</a> for full stop-and-go capabilities (requires also a <a class="thm-color-two" href="tinkla_pedal">Tinkla Pedal Interceptor</a>)</li>
<ul>
<hr/>
<h5 class="thm-color-one">About EON Clones</h5>
<ul>
<p> There are a few <a class="thm-color-two" href="https://www.aliexpress.com/item/1005003224622357.html" target="_new">EON Clones</a> out there available to purchase. While the software might work on them, I do not guarantee anything, nor do I support it directly. Also, the <a class="thm-color-two" href="t/tinkla_obdc.html">Tinkla OBD-C Adapter</a> <b class="thm-color-one">will NOT work</b> with the <a class="thm-color-two" href="https://www.aliexpress.com/item/1005003966793202.html" target="_new">Black Panda clone</a>, so you will have to make your own adapter from that box to your preAP Tesla Model S. If you chose to go that route, better ask users in our <a class="thm-color-two" href="https://discord.com/invite/avCJxEX" target="_new">Discord group</a> for support. I will only provide support for official Comma hardware.</p>
</ul>
<hr/>
</ul></ul></div>
