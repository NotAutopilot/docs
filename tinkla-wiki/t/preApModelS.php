<h2 class="post_title">Features and Limitations</h2>
<div class="blog_meta">
<p>Back in the day (circa 2018, haha) when we started using OpenPilot for preAP Tesla Model S, installing the components needed took quite some time and required taking apart the frunk, drilling holes through the firewall to run cables, creating your own harness, etc. Then you had to install the software by "ssh-ing" into the unit, run complex commands to set it up and, if lucky, you would have no errors and maybe time the same day to put your car back together. Then came the "Panda dance", a sequece of commands, with some "now open door and lift but off seat" in between to get the damn device to flash the code. And then, after all this, you got your steering wheel to move by itself.</p>
<p>Fast forward to 2022 and the whole solution looks nothing like 4 years ago. There is no need for the "EPAS harness" thus no need to take your frunk apart. To go even further, there is no need for you to touch a screwdrier or a wrench. All  you have to do is to attach the mounting bracket to the windshile, run a long USB cable from the mirror to the driver footwell and plug it in the OBD2 port using the <a class="thm-color-two" href="t/tinkla_obdc.html">Tinkla OBD-C Adapter</a>. That is all you need to get Lane Keep Assist, Automatic Lane Change, Forward Collision Warnings and limitted Adaptive Cruise Control (read below why we call this "limitted"). But you always also have the option to control the acceleration and braking by foot as you normally do and let OpenPilot only take care of the steering. Software installation, upgrades and configuration are all done now through the UI. No more need to be a computer wiz to get your openpilot installed.</p>
<p>By adding a <a class="thm-color-two" href="t/tinkla_buddy.html">Tinkla Buddy</a> you can improve the experience by having AutoPilot like information displayed on your Instrument Cluster. Lead car, lanes, curvature, extended messages, all these are avaiable through the <a class="thm-color-two" href="t/tinkla_buddy.html">Tinkla Buddy</a> interface. (<b class="thm-color-one">Important note:</b>: Tinkla Buddy only works with MCU1 cars. If your car has MCU2, there is no solution for IC integration. You can stil use OpenPilot and benefit from all its features.)</p>
<p>You can be even more adventurous and retrofit a <a class="thm-color-two" href="tinkla_radar">Tinkla Radar Kit</a> to improve the detection of objects in front of you. All this is documented in <a class="thm-color-two" href="index_php/Tesla_Bosch_Radar.html" target="_new">our WiKi</a>, but it does require taking apart your frunk.</p>
<p>Another fairly easy upgrade would be to add a <a class="thm-color-two" href="index_php/Pedal_Interceptor.html" target="_new">Pedal Interceptor</a>. The Pedal Interceptor allows the Adaptive Cruise Control to go down to 5MPH (while the standard Tesla Cruise Control only operates above 18MPH). There is work being done to combine control of retrofitted iBooster (electric brakes) that, together with the Pedal Interceptor to really create a full stop-and-go experience for preAP Tesla Model S.</p>
<p>And now let's touch on the limitations of the longitudinal control. On preAP Tesla Model S, the braking system is vacuum based, like on regular ICE cars. For AutoPilot equiped cars, Tesla introduced the iBooster, and electric-hydraulic braking system that can be controlled electronically. That is why AP cars are capable of full stop-and-go while preAP are not. The way we implemented the Adaptive Cruise Control for OpenPilot is by controlling and adjusting the cruise conntrol of your car. This allows us to accelerate and decelerate, but the deceleration is only achieved using the regeneratie brakes of the car. This has limitations of how fast it can brake the car. So the driver has to be ready at any moment to take control and apply the brakes.</p>
</div>
<hr/>
<p><b class="thm-color-one">Breaking News:</b> Support for <a class="thm-color-two" href="https://sghinnovations.com/?product=ibooster-controller-ecu" target="_new">SGH Innovations' iBooster Controller ECU</a> is available now in the Unity branch. This provides full stop-and-go capabilities to pre AutoPilot Tesla Model S equipped with iBooste and the iBooster Controller ECU.</p>
