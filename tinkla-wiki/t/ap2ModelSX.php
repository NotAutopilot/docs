<hr/>
<h2 class="post_title">Features and Capabilities</h2>
<div class="blog_meta">
<p>When it comes to AP2/2.5/3, you start thinking: what can OpenPilot with its 3 cameras can offer on top of my 7-8-9 cameras? Well, depends on what you paid for and what is enabled on your car...</p>
<p>AutoPilot hardware 3 (and the future hardware 4 version) have amazing capabilities. If you have the full capabilities, not sure we can offer much on top of that. But if you did not pay for FSD, then there might be something in there for you...</p>
<p>The AP2+ code is developed (and maintained) by Comma. That means AP2 Tesla is a fully supported car on the base OpenPilot branch. And with the new developments with e2e longitudinal control with experimental features such as stopping for stop signs and stop lights and soon the "nav on maps" capabilities, this is a strong alternative to the basic features of AP2.</p>
<p>We incorporated the AP2 code in our branch as well, to offer the full integration into the instrumeint cluster (IC), allowing coexistance of OpenPilot and AutoPilot (not on the OP branch) as well as adding our standard features like human steer override, automatic lane change with blind spot monitoring, human acceleration oveeride, etc.</p>
<p>In short, you have so much to gain and nothing to lose by adding OpenPilot to your AP2 Model S or X. :)</p>
